        static readonly uint[] Rcon = { 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36, 0x6C, 0xD8, 0xAB, 0x4D, 0x9A, 0x2F, 0x5E, 0xBC, 0x63, 0xC6, 0x97, 0x35, 0x6A, 0xD4, 0xB3, 0x7D, 0xFA, 0xEF, 0xC5, 0x91 };

        const int Nb = 8;
        const int Nk = 8;
        const int Nr = 14;
        const int ExpandedKeySize = Nb * (Nr + 1) * 4;

        static readonly byte[] SRInv = { 0, 7, 5, 4 };

        static byte G8Mul(byte a, byte b)
        {
            byte p = 0;
            for (int i = 0; i < 8; i++)
            {
                if ((b & 1) != 0) p ^= a;
                bool hi = (a & 0x80) != 0;
                a <<= 1;
                if (hi) a ^= 0x1B;
                b >>= 1;
            }
            return p;
        }

        static uint SubWord(uint w)
        {
            return (uint)(SBox[w & 0xFF] |
                         (SBox[(w >> 8) & 0xFF] << 8) |
                         (SBox[(w >> 16) & 0xFF] << 16) |
                         (SBox[(w >> 24) & 0xFF] << 24));
        }

        static uint RotWord(uint w)
        {
            return (w >> 8) | (w << 24);
        }

        static byte[] KeyExpansion(byte[] key)
        {
            uint[] w = new uint[ExpandedKeySize / 4];
            for (int i = 0; i < Nk; i++)
                w[i] = (uint)(key[4 * i] | (key[4 * i + 1] << 8) | (key[4 * i + 2] << 16) | (key[4 * i + 3] << 24));

            for (int i = Nk; i < Nb * (Nr + 1); i++)
            {
                uint temp = w[i - 1];
                if (i % Nk == 0)
                    temp = SubWord(RotWord(temp)) ^ Rcon[i / Nk - 1];
                else if (Nk > 6 && i % Nk == 4)
                    temp = SubWord(temp);
                w[i] = w[i - Nk] ^ temp;
            }

            byte[] ek = new byte[ExpandedKeySize];
            for (int i = 0; i < w.Length; i++)
            {
                ek[4 * i] = (byte)(w[i] & 0xFF);
                ek[4 * i + 1] = (byte)((w[i] >> 8) & 0xFF);
                ek[4 * i + 2] = (byte)((w[i] >> 16) & 0xFF);
                ek[4 * i + 3] = (byte)((w[i] >> 24) & 0xFF);
            }
            return ek;
        }

        static void AddRoundKey(byte[] state, int stateOff, byte[] rk, int rkOff, int len)
        {
            for (int i = 0; i < len; i++)
                state[stateOff + i] ^= rk[rkOff + i];
        }

        static void InvSubBytes(byte[] state, int off, int len)
        {
            for (int i = 0; i < len; i++)
                state[off + i] = SBoxInv[state[off + i]];
        }

        static void InvShiftRows(byte[] state, int off)
        {
            byte[] t = new byte[Nb * 4];
            Array.Copy(state, off, t, 0, Nb * 4);
            for (int r = 0; r < 4; r++)
            {
                for (int c = 0; c < Nb; c++)
                {
                    int srcCol = (c + SRInv[r]) % Nb;
                    state[off + r + 4 * c] = t[r + 4 * srcCol];
                }
            }
        }

        static void InvMixColumns(byte[] state, int off)
        {
            for (int c = 0; c < Nb; c++)
            {
                int colOff = off + 4 * c;
                byte s0 = state[colOff], s1 = state[colOff + 1], s2 = state[colOff + 2], s3 = state[colOff + 3];
                state[colOff]     = (byte)(G8Mul(0x0E, s0) ^ G8Mul(0x0B, s1) ^ G8Mul(0x0D, s2) ^ G8Mul(0x09, s3));
                state[colOff + 1] = (byte)(G8Mul(0x09, s0) ^ G8Mul(0x0E, s1) ^ G8Mul(0x0B, s2) ^ G8Mul(0x0D, s3));
                state[colOff + 2] = (byte)(G8Mul(0x0D, s0) ^ G8Mul(0x09, s1) ^ G8Mul(0x0E, s2) ^ G8Mul(0x0B, s3));
                state[colOff + 3] = (byte)(G8Mul(0x0B, s0) ^ G8Mul(0x0D, s1) ^ G8Mul(0x09, s2) ^ G8Mul(0x0E, s3));
            }
        }

        static void DecryptBlock(byte[] input, int inOff, byte[] output, int outOff, byte[] eks)
        {
            Array.Copy(input, inOff, output, outOff, Nb * 4);
            int roundKeyOff = Nb * 4 * Nr;
            AddRoundKey(output, outOff, eks, roundKeyOff, Nb * 4);

            for (int r = Nr - 1; r >= 1; r--)
            {
                InvShiftRows(output, outOff);
                InvSubBytes(output, outOff, Nb * 4);
                roundKeyOff = Nb * 4 * r;
                AddRoundKey(output, outOff, eks, roundKeyOff, Nb * 4);
                InvMixColumns(output, outOff);
            }

            InvShiftRows(output, outOff);
            InvSubBytes(output, outOff, Nb * 4);
            AddRoundKey(output, outOff, eks, 0, Nb * 4);
        }

        public static byte[] DecryptCbcPkcs7(byte[] ciphertext, byte[] key, byte[] iv)
        {
            if (ciphertext.Length % (Nb * 4) != 0)
                throw new ArgumentException("Ciphertext length must be multiple of block size (32 bytes)");

            byte[] eks = KeyExpansion(key);
            byte[] plain = new byte[ciphertext.Length];
            byte[] prev = new byte[Nb * 4];
            Array.Copy(iv, 0, prev, 0, Nb * 4);

            for (int off = 0; off < ciphertext.Length; off += Nb * 4)
            {
                byte[] decBlock = new byte[Nb * 4];
                DecryptBlock(ciphertext, off, decBlock, 0, eks);

                for (int i = 0; i < Nb * 4; i++)
                    plain[off + i] = (byte)(decBlock[i] ^ prev[i]);

                Array.Copy(ciphertext, off, prev, 0, Nb * 4);
            }

            int padLen = plain[plain.Length - 1];
            if (padLen < 1 || padLen > Nb * 4)
                throw new ArgumentException("Invalid PKCS7 padding");

            for (int i = plain.Length - padLen; i < plain.Length; i++)
            {
                if (plain[i] != padLen)
                    throw new ArgumentException("Invalid PKCS7 padding");
            }

            byte[] result = new byte[plain.Length - padLen];
            Array.Copy(plain, 0, result, 0, result.Length);
            return result;
        }
